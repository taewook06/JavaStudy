package a_variable;

public class Hello {
	/* 공통 
	   - 자바 프로그램의 시작점 main 메서드
	   - 중괄호 { } 블록으로 감싸고 있음
	   - outline 현재 작성중인 코드에 있는 구성요소의 계층화
	     C아이콘 : 클래스(Class)
	     V아이콘 : 변수(Variable)	     
	     M아이콘 : 메서드(Method)
	     S아이콘 : 스태틱(static)
	*/ 
	public static void main(String[] args) {
		System.out.println("hello world");
	}
}